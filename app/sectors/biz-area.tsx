/* 부문 블록 — note가 있으면 제목 옆 "?" 버튼으로 보조설명을 블록마다 독립 토글 */
function DivisionBlock({ d }: { d: BizDivision }) {
  const [noteOpen, setNoteOpen] = useState(false);
  return (
    <div>
      <h3 className="mb-1.5 flex items-center gap-1.5 text-sm font-semibold">
        {d.title}
        {d.note && (
          <button
            type="button"
            onClick={() => setNoteOpen((v) => !v)}
            aria-label="보조설명"
            aria-expanded={noteOpen}
            title="보조설명"
            className="inline-flex h-4 w-4 shrink-0 items-center justify-center rounded-full border border-border text-[10px] font-bold leading-none text-muted-foreground transition-colors hover:bg-muted hover:text-foreground"
          >
            ?
          </button>
        )}
      </h3>
      <p className="border-l-2 border-border pl-3 text-xs leading-relaxed text-muted-foreground">
        {d.body}
      </p>
      {d.note && noteOpen && (
        <p className="ml-3 mt-1.5 rounded border-l-2 border-border bg-muted/50 px-3 py-2 text-[11px] leading-relaxed text-muted-foreground">
          {d.note}
        </p>
      )}
    </div>
  );
}

function BizAreaModal({
  name,
  divisions,
  onClose,
}: {
  name: string;
  divisions: BizDivision[];
  onClose: () => void;
}) {
  useEffect(() => {
    const onKey = (e: KeyboardEvent) => {
      if (e.key === "Escape") onClose();
    };
    document.addEventListener("keydown", onKey);
    document.body.style.overflow = "hidden";
    return () => {
      document.removeEventListener("keydown", onKey);
      document.body.style.overflow = "";
    };
  }, [onClose]);

  return (
    <div
      className="fixed inset-0 z-[200] flex items-center justify-center bg-black/50 p-4"
      onClick={onClose}
      role="dialog"
      aria-modal="true"
    >
      <div
        className="flex max-h-[80vh] w-full max-w-md flex-col overflow-hidden rounded-xl border border-border bg-card shadow-2xl"
        onClick={(e) => e.stopPropagation()}
      >
        <div className="flex items-center justify-between gap-2 border-b border-border px-5 py-4">
          <h2 className="text-base font-bold">{name} 사업영역</h2>
          <button
            onClick={onClose}
            aria-label="닫기"
            className="shrink-0 rounded p-1 text-muted-foreground hover:bg-muted"
          >
            <X className="h-4 w-4" />
          </button>
        </div>
        <div className="space-y-4 overflow-y-auto px-5 py-4">
          {divisions.map((d) => (
            <DivisionBlock key={d.title} d={d} />
          ))}
        </div>
      </div>
    </div>
  );
}

/* 종목 행에서 회사명 옆 아이콘 — 사업영역 데이터가 있는 종목에만 렌더(없으면 null) */
export function BizAreaIcon({ code, name }: { code: string; name: string }) {
  const [open, setOpen] = useState(false);
  const divisions = BIZ_AREAS[code];
  if (!divisions) return null;

  return (
    <>
      <button
        type="button"
        onClick={() => setOpen(true)}
        aria-label={`${name} 사업영역 보기`}
        title="사업영역"
        className="shrink-0 text-muted-foreground transition-colors hover:text-primary"
      >
        <Layers className="h-3.5 w-3.5" />
      </button>
      {open && <BizAreaModal name={name} divisions={divisions} onClose={() => setOpen(false)} />}
    </>
  );
}
